n = input('\033[7;97mDigite algo:\033[m ')
print('\033[30mO tipo primitivo desse valor é {}\033[m'.format(type(n)))
print('\033[31mO Valor é maiúsculo? {}\033[m'.format(n.isupper()))
print('\033[32mO Valor é minúsculo? {}\033[m'.format(n.islower()))
print('\033[33mO Valor só tem espaços? {}\033[m'.format(n.isspace()))
print('\033[34mO valor é um número? {}\033[m'.format(n.isnumeric()))
print('\033[35mO valor é alfabético? {}\033[m'.format(n.isalpha()))
print('\033[36mO valor é alfanúmerico? {}\033[m'.format(n.isalnum()))
print('\033[37mO valor está capitalizado? {}\033[m'.format(n.istitle()))
